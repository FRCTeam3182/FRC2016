package org.usfirst.frc.team3182.robot.util;

public class Color {

	private int r=0;
	private int g=0;
	private int b=0;
	public Color(int r, int g, int b) {
		this.r = r;
		this.g = g;
		this.b = b;
	}
	
	public int getRed(){
		return r;
	}
	public int getGreen(){
		return g;
	}
	public int getBlue(){
		return b;
	}

}
